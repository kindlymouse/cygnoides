package com.metarnet.fusionchart.exception;

public class NotChartException extends Exception {
	public NotChartException() {
		super();
	}

	public NotChartException(String msg) {
		super(msg);
	}

	public NotChartException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public NotChartException(Throwable cause) {
		super(cause);
	}

}
